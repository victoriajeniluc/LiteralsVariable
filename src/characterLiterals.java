public class characterLiterals {
    public static void main(String[] args) {
        System.out.println('a');

        System.out.print("->");
        System.out.print("\n");
        System.out.println("<-");

        System.out.print("->");
        System.out.print("\t");
        System.out.println("<-");

        System.out.print("->");
        System.out.print("\\");
        System.out.println("<-");

        System.out.print("->");
        System.out.print("\'");
        System.out.println("<-");

        System.out.println("\u0041"); // character A
        System.out.println("\u0968"); // Devangari
        System.out.println("\u23F3"); // hourglass
        System.out.println("\u20B9"); // indian rupree symbol
    }
}
