public class characterLiterals {
    public static void main(String[] args) {
        System.out.println('a');

        System.out.print("->");
        System.out.print("\n");
        System.out.println("<-");

        System.out.print("->");
        System.out.print("\t");
        System.out.println("<-");

        System.out.print("->");
        System.out.print("\\");
        System.out.println("<-");

        System.out.print("->");
        System.out.print("\'");
        System.out.println("<-");
    }
}
