/**
 * Class to show the floating point literal representations in Java.
 */


public class FloatingPointLiterals {
    public static void main(String[] args){
        System.out.println(74589787.12); // standard
        System.out.println(7.458978712E7); // scientific

        System.out.println(0.314567821); // standard
        System.out.println(3.12567821E-1); // scientific
    }
}
